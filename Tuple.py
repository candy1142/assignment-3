# tuples in python
academic_tuple = ('name', 'father name', 'reg number', 'CNIC number', 'CGPA')
print(academic_tuple)

# single item tuple
Mr_A_registration = ("SP24-BBA-59")
print(Mr_A_registration)
                     
# tuple of integer
numbers_tuple = (1,2,3,4)
print(numbers_tuple)

#tuple of strings
fruits_tuple = ('apple', 'banana', 'cherry')
print(fruits_tuple)


# mixed tuple
mixed_tuple = (1, 'apple', 3.14, True)
print(mixed_tuple)

# tuple with a single item
single_item_tuple = (5,)
print(single_item_tuple)

# accessing tuple elements
fruits = ('apple', 'banana', 'cherry')
print(fruits_tuple[0])
print(fruits_tuple[1])

# negative indexing
print(fruits_tuple[-1])
print(fruits_tuple[-2])

# tuple operation

# Concatenation
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
combined_tuple = tuple1 + tuple2
print(combined_tuple)

# repetition
tuple1 = ('hello',)
repeated_tuple = tuple1 * 3
print(repeated_tuple)

# tuple slicing
numbers = (0, 1, 2, 3, 4, 5, 6)
print(numbers[1:4])
print(numbers[:3])
print(numbers[4:])


# tuple method

# count(item)
fruits_tuple = ('apple', 'banana', 'cherry', 'apple')
print(fruits_tuple.count('apple'))

#index(item)
print(fruits.index('cherry'))



















